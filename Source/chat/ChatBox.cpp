void ChatBox::SendFile(File file)
{
	// check max size
	//if (file.getSize() > 1000 * jmin<int>(maxETx, MAX_SEND_KB)) // 100 kB
	if (file.getSize() > 1000 * SOCKET_MAX_SEND_KB) // in kB
	{
		String more = (//maxETx < SOCKET_MAX_SEND_KB ? "\nMore coins needed for larger file tranfers !" :
			"\nMax size is " + String(SOCKET_MAX_SEND_KB) + " kB !");
		NativeMessageBox::showMessageBox(AlertWindow::WarningIcon, ProjectInfo::projectName, "File is too large !" + more);
		return;
	}
	MessageList *messageList = (MessageList *)messageViewport->getViewedComponent();

	juce::Rectangle<int> r = messageViewport->getViewArea();
	messageList->AddUnconfirmedSendMessage(file.getFileName());

	listeners.call(&ChatComponentListener::SocketSendFile, receiverChannel, file, isPrivateChannel);
}

void ChatBox::SendMessage(String msg)
{
	MessageList *messageList = (MessageList *)messageViewport->getViewedComponent();
	if (msg.isEmpty())
		msg = messageEditor->getText();

	messageList->AddUnconfirmedSendMessage(msg);
	listeners.call(&ChatComponentListener::SocketSendMessage, receiverChannel, msg, isPrivateChannel);

	messageEditor->setText("", false);
}
